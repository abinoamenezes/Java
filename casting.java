public class casting {
    public static void main(String[] args) {
        //cassting implicito
        int x= 10;

        System.out.println(x);

        double y= x;

        System.out.println(y);

        //casting explicito

        double g=10.5;
        System.out.println(g);
        int f= (int) g;
        System.out.println(f);


    }
}
