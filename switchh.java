public class switchh {
    public static void main(String[] args) {
       int opcao=2;

       switch (opcao) {
        case 1:
            System.out.println("Voce selecionou a opção 1");
            break;
        case 2: 
            System.out.println("Voce selecionou a opção 2");
            break;
        case 3:
            System.out.println("Voce selecionou a opção 3");
            break;
       }
    }
}
